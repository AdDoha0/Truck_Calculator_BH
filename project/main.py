# BH Trans - Truck Dashboard Application
# 
# Многостраничное Streamlit приложение для отчётности по тракам
# 
# Структура:
# project/frontend/
# ├── app.py                    # Главная страница
# ├── ui_utils.py               # Утилиты и стили
# └── pages/
#     ├── 1_Upload.py           # Загрузка данных
#     ├── 2_Report.py           # Отчёты и графики
#     ├── 3_Settings.py         # Настройки
#     └── 4_References.py       # Справочники
#
# Для запуска приложения:
# 1. Активируйте виртуальное окружение:
#    source venv/bin/activate
#
# 2. Запустите Streamlit:
#    streamlit run project/frontend/app.py
#
# 3. Откройте браузер по адресу:
#    http://localhost:8501

